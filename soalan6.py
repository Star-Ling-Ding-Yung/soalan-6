markah=int(input("Sila masukkan markah murid:"))

if 0<= markah <=40:
    print("Anda ditempatkan di kelas Dedikasi")
    
elif 41<= markah <=60:
    print("Anda ditempatkan di kelas Cerdik")

elif 61<= markah <=80:
    print("Anda ditempatkan di kelas Bijak")

else:
    print("Anda ditempatkan di kelas Amanah")
